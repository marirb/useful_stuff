def multiply_colormap(cmap_list, n):
    cmap_new = []
    for item in cmap_list:
        cmap_new.extend([item] * n)
    return cmap_new

def discrete_cmap(cmap_name, N, low=0, up=1):
    """Create an N-bin discrete colormap from the specified input map"""
    cmap = cm.get_cmap(cmap_name)
    return cmap(np.linspace(low,up,N))


